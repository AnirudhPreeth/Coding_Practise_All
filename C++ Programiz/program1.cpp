/*
"Hello World" program. 
*/

#include<iostream>
int main() {
    std::cout<<"Hello World!";
    return 0;
}


/*
# include is preprocessor directive used to include files in the program. 
Preprocessing directives are lines in your program that start with #. 
The # is followed by an identifier that is the directive name.

This allows us to use iostream. And get the contents of iostream. 
This allows us to use cout in our program to print output on the screen.
We need to use #include <iostream> to use cout that allows us to print output on the screen.

std::cout - Prints the content inside the quotation marks. 
It must be followed by << followed by the format string.

The return 0; statement is the "Exit status" of the program. 
In simple terms, the program ends with this statement.
*/ 

