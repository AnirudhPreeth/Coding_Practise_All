/*
 Add Two Numbers. 
*/

#include<iostream> 
using namespace std;

int main() {
    int first_number, second_number, sum;
    cout<<"Enter the two numbers : ";
    cin>>first_number>>second_number;

    //Sum of two numbers is :
    sum = first_number + second_number;

    //Print sum. 
    cout<<first_number<<"+"<<second_number<<"="<<sum;

    return 0;
}

/*
First_number - 1'st integer. 
Second_number - 2'nd integer.

+ operator - Used to add the integers.
Then displayed sum on screen. 
*/